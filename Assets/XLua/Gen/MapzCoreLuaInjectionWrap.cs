					Mapz.Core.Lua.Injection gen_ret = new Mapz.Core.Lua.Injection();
					translator.Push(L, gen_ret);
                    
					return 1;
				}
				
			}
			catch(System.Exception gen_e) {
				return LuaAPI.luaL_error(L, "c# exception:" + gen_e);
			}
            return LuaAPI.luaL_error(L, "invalid arguments to Mapz.Core.Lua.Injection constructor!");
            
        }
        
		
        
		
        
        
        
        
        
        
        
